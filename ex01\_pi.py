import math                     # Importe le module math (fonctions + constantes)
x = math.pi                     # Stocke la constante pi dans la variable x

print(math.pi)                  # Affiche pi "au naturel" (beaucoup de decimales)
print(format(x, ".3g"))         # Affiche x avec 3 chiffres significatifs (format general)
print(format(x, ".3f"))         # Affiche x avec 3 chiffres apres la virgule (format flottant)
print(format(x, ".5f"))         # Affiche x avec 5 chiffres apres la virgule
print(format(x, ".5g"))         # Affiche x avec 5 chiffres significatifs
